using System;

namespace Util {

    /// <summary>
    /// Klasse mit Erweiterungsmethoden
    /// </summary>
    public static class Extensions {

        /// <summary>
        /// Liefert Radianten zu einem übergebenen Grad.
        /// </summary>
        /// <param name="degrees">die Gradzahl zu der der radiant geliefert werden soll.</param>
        /// <returns>die Radianten für die übergebene Gradzahl</returns>
        public static double DegreeToRadian(this double degrees) {
            return Math.PI * (degrees / 180.0);
        }
    }
}